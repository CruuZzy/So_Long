#include "../includes/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*auxt;

	auxt = *(lst);
	if ((*lst))
	{
		while (auxt->next != NULL)
		{
			auxt = auxt->next;
		}
		auxt->next = new;
	}
	if (!*(lst))
	{
		*(lst) = new;
	}
}
