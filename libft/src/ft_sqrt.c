#include "../includes/libft.h"

int	ft_sqrt(int n)
{
	int	c;

	if (n < 4)
		return (1);
	c = 2;
	while (c * c < n)
		c++;
	if (c * c > n)
	{
		if ((c * c - n) < ((c - 1) * (c - 1) + (-n)))
			return (c);
	}
	return (c - 1);
}
